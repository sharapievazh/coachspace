import { defineTool } from "@lovable.dev/mcp-js";
import { z } from "zod";

const QUESTIONS: Record<"G" | "R" | "O" | "W", string[]> = {
  G: [
    "Какую цель ты хочешь достичь по итогу сессии?",
    "Как ты поймёшь, что цель достигнута? Каков измеримый результат?",
    "Почему эта цель важна именно сейчас?",
    "Как эта цель соотносится с твоими долгосрочными приоритетами?",
    "Что изменится в твоей жизни, когда цель будет достигнута?",
  ],
  R: [
    "Что происходит сейчас? Опиши ситуацию фактами.",
    "Что ты уже пробовал(а)? Что сработало, а что — нет?",
    "Какие ресурсы у тебя уже есть?",
    "Что тебя останавливает? Что мешает двигаться дальше?",
    "Как ты сам(а) влияешь на текущее положение дел?",
  ],
  O: [
    "Какие варианты действий ты видишь?",
    "Что бы ты сделал(а), если бы точно знал(а), что получится?",
    "Какой совет ты дал(а) бы другу в такой ситуации?",
    "Что ещё? А если бы был неограниченный бюджет / время?",
    "Какие плюсы и минусы у каждого варианта?",
  ],
  W: [
    "Какой вариант ты выбираешь?",
    "Какой первый конкретный шаг ты сделаешь и когда?",
    "Что может тебе помешать? Как ты с этим справишься?",
    "Кто может тебя поддержать в этом?",
    "По шкале 1–10, насколько ты готов(а) действовать? Что поднимет цифру на балл выше?",
  ],
};

export default defineTool({
  name: "grow_questions",
  title: "GROW: коучинговые вопросы",
  description:
    "Возвращает подборку сильных коучинговых вопросов по модели GROW для одного из этапов: Goal, Reality, Options, Will.",
  inputSchema: {
    step: z
      .enum(["G", "R", "O", "W"])
      .describe("Этап GROW: G = Goal, R = Reality, O = Options, W = Will"),
  },
  annotations: { readOnlyHint: true, idempotentHint: true, openWorldHint: false },
  handler: ({ step }) => ({
    content: [
      {
        type: "text",
        text: QUESTIONS[step].map((q, i) => `${i + 1}. ${q}`).join("\n"),
      },
    ],
    structuredContent: { step, questions: QUESTIONS[step] },
  }),
});
